package edyoda.javaclasses;

public class Calculator {

    public int add(int a, int b){
        return a + b;
    }

    public int add(int a, int b, int c){
        return a + b + c;
    }

    public int diff(int a, int b){
        return a - b;
    }

    public int diff(int a, int b, int c){
        return a - b - c;
    }

}
